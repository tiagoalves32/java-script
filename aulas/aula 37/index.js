
// Function expression
const souUmDado = function() {
    console.log('Sou um dado.')
}

// Arrow Function
const funcaoArrow = () => {
    console.log('Sou uma arrow function')
}
funcaoArrow()

// Dentro de um objeto

const obj = {
    falar() {
        console.log('Estou falando')
    }
}
obj.falar()